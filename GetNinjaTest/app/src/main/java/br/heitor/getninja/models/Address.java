package br.heitor.getninja.models;

public class Address {
    private String city;
    private String street;
    private String neighborhood;
    private String uf;

    public String getCity() {
        return city;
    }

    public String getStreet() {
        return street;
    }

    public String getNeighborhood() {
        return neighborhood;
    }

    public String getUf() {
        return uf;
    }
}
